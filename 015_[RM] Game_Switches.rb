#==============================================================================
# ** Game_Switches
#------------------------------------------------------------------------------
#  Esta classe gerencia os switches.
# A instância desta classe é referenciada por $game_switches.
#==============================================================================

class Game_Switches
  #--------------------------------------------------------------------------
  # * Inicialização do objeto
  #--------------------------------------------------------------------------
  def initialize
    @data = []
  end
  #--------------------------------------------------------------------------
  # * Aquisição do switch
  #     switch_id : ID do switch
  #--------------------------------------------------------------------------
  def [](switch_id)
    @data[switch_id] || false
  end
  #--------------------------------------------------------------------------
  # * Configuração do switch
  #     switch_id : ID do switch
  #     value     : ON (true) / OFF (false)
  #--------------------------------------------------------------------------
  def []=(switch_id, value)
    @data[switch_id] = value
    on_change
  end
  #--------------------------------------------------------------------------
  # * Quando houve alteração
  #--------------------------------------------------------------------------
  def on_change
    $game_map.need_refresh = true
  end
end
 