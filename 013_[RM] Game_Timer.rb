#==============================================================================
# ** Game_Timer
#------------------------------------------------------------------------------
#  Esta classe gerencia o cronômetro.
# A instância desta classe é referenciada por $game_timer.
#==============================================================================

class Game_Timer
  #--------------------------------------------------------------------------
  # * Inicialização do objeto
  #--------------------------------------------------------------------------
  def initialize
    @count = 0
    @working = false
  end
  #--------------------------------------------------------------------------
  # * tualização da tela
  #--------------------------------------------------------------------------
  def update
    if @working && @count > 0
      @count -= 1
      #on_expire if @count == 0
    end
  end
  #--------------------------------------------------------------------------
  # * Inicialização
  #    count : contador
  #--------------------------------------------------------------------------
  def start(count)
    @count = count
    @working = true
  end
  #--------------------------------------------------------------------------
  # * Parar
  #--------------------------------------------------------------------------
  def stop
    @working = false
  end
  #--------------------------------------------------------------------------
  # * Definição de em operação
  #--------------------------------------------------------------------------
  def working?
    @working
  end
  #--------------------------------------------------------------------------
  # * Aquisição dos segundos
  #--------------------------------------------------------------------------
  def sec
    @count / Graphics.frame_rate
  end
=begin
  #--------------------------------------------------------------------------
  # * Quando cronômetro chegar a zero
  #--------------------------------------------------------------------------
  def on_expire
    BattleManager.abort
  end
=end
end
